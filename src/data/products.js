export function getProductById(id) {
  return products.find(product => product.id === parseInt(id))
}

export function getProductsByCategory(category) {
  if (category === 'all') return products
  return products.filter(product => product.category === category)
}

export function getProductsByBrand(brand) {
  return products.filter(product => product.brand === brand)
}

export function searchProducts(query) {
  const searchTerm = query.toLowerCase()
  return products.filter(product => 
    product.name.toLowerCase().includes(searchTerm) ||
    product.description.toLowerCase().includes(searchTerm) ||
    product.category.toLowerCase().includes(searchTerm) ||
    product.brand.toLowerCase().includes(searchTerm)
  )
}

export function getFeaturedProducts(limit = 8) {
  return products
    .filter(p => p.rating >= 4.5 && p.inStock)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit)
}

export function getOnSaleProducts(limit = 8) {
  return products
    .filter(p => p.originalPrice > p.price && p.inStock)
    .sort((a, b) => (b.originalPrice - b.price) - (a.originalPrice - a.price))
    .slice(0, limit)
}

export function getNewArrivals(limit = 8) {
  return products
    .slice(-limit)
    .reverse()
}

export function getBestSellers(limit = 8) {
  return products
    .filter(p => p.reviews >= 100 && p.inStock)
    .sort((a, b) => b.reviews - a.reviews)
    .slice(0, limit)
}
